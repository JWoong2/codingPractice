package april.week4.Ex5.FrameWorkProblem;

public class Board {
	private String title;
	private String content;
	public Board(String title, String content) {
		super();
		this.title = title;
		this.content = content;
	}
	public String getContent() {
		return content;
	}
	public String getTitle() {
		return title;
	}
	
	
}
